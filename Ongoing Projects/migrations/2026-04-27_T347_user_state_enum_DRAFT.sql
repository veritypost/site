--        state. AccountStateBanner already implements severity for the
--        legacy 6-state set. Multi-state stacking lost.
--     B) Bitmask column instead of enum. Each bit a state. Cheaper
--        DB-side, harder to read in queries.
--     C) Sidecar table users_states (user_id, state, since, until)
--        with multi-row semantics. Most flexible, most disruptive.
