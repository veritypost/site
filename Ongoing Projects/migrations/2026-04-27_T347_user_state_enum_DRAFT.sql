-- Problem (per system map §8):
--   public.users carries 8 independent fields that each represent a
--   user-state dimension:
--     is_banned            (bool)
--     locked_until         (timestamptz, future)
--     is_muted             (bool)
--     muted_until          (timestamptz, future)
--     deletion_scheduled_for (timestamptz, future)
--     frozen_at            (timestamptz, past)
--     plan_grace_period_ends_at (timestamptz, future)
--     verify_locked_at     (timestamptz, past)
--   Plus comped_until (timestamptz, future) — a 9th in some readings.
--
--   They're not synchronized. A user can have any combination set; the
--   AccountStateBanner picks one for display (T305 just shipped the
--   multi-state stack), but no DB-level CHECK enforces that mutually-
--   exclusive states (banned + frozen, deletion_scheduled + grace) make
--   sense.
--
-- Proposed shape:
--   New ENUM `user_state`:
--     'active'                    — happy path (default)
--     'unverified'                — email_verified=false (still readable)
--     'verify_locked'             — failed too many verify attempts
--     'banned'                    — hard block, no time bound
--     'login_locked'              — temporary lock-out (with locked_until)
--     'muted'                     — soft penalty (with muted_until)
--     'frozen'                    — score frozen (cancelled paid plan)
--     'plan_grace'                — grace period before freeze (with end-date)
--     'deletion_scheduled'        — 30-day deletion window
--     'comped'                    — admin-granted Pro window
--
--   New column users.state user_state NOT NULL DEFAULT 'active'.
--   Sidecar timestamp columns survive (locked_until, muted_until, etc.)
--   because the state is "what" and the timestamp is "until when."
--
--   Multi-state users (banned + frozen) need a different model. Two
--   options surfaced for owner pick:
